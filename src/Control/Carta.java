public class Carta {
	
	private int linha;
	private int coluna;
	private String numCarta;
	
	
	public int getLinha() {
		return linha;
	}
	public void setLinha(int linha) {
		this.linha = linha;
	}
	public int getColuna() {
		return coluna;
	}
	public void setColuna(int coluna) {
		this.coluna = coluna;
	}
	public String getNumCarta() {
		return numCarta;
	}
	public void setNumCarta(String numCarta) {
		this.numCarta = numCarta;
	}
	
	

}
